package _2_classes;

public class GSM {
	
	private String model;
	private String display;
	private String processor;
	private String battery;
	private boolean isWifiChargable;
	/**
	 * @return the model
	 */
	public String getModel() {
		return model;
	}
	/**
	 * @param model the model to set
	 */
	public void setModel(String model) {
		this.model = model;
	}
	/**
	 * @return the display
	 */
	public String getDisplay() {
		return display;
	}
	/**
	 * @param display the display to set
	 */
	public void setDisplay(String display) {
		this.display = display;
	}
	/**
	 * @return the processor
	 */
	public String getProcessor() {
		return processor;
	}
	/**
	 * @param processor the processor to set
	 */
	public void setProcessor(String processor) {
		this.processor = processor;
	}
	/**
	 * @return the battery
	 */
	public String getBattery() {
		return battery;
	}
	/**
	 * @param battery the battery to set
	 */
	public void setBattery(String battery) {
		this.battery = battery;
	}
	/**
	 * @return the isWifiChargable
	 */
	public boolean isWifiChargable() {
		return isWifiChargable;
	}
	/**
	 * @param isWifiChargable the isWifiChargable to set
	 */
	public void setWifiChargable(boolean isWifiChargable) {
		this.isWifiChargable = isWifiChargable;
	}
	
	
	
	

}
